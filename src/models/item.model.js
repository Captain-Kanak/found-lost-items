import mongoose from "mongoose";

// Define the item schema
const itemSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
});

// Create the item model
const Item = mongoose.model("Item", itemSchema);

// Export the item model
export default Item;
